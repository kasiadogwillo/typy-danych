public class TypyDanychLekcja1 {
    public static void main(String[] args) {
        //start aplikacji - wykonanie kodu - metoda main

        //liczby całkowite
        System.out.println(10);

        //liczby zmiennoprzecinkowe - z kropką
        System.out.println(10.567);

        //tekst - string - w cudzysłowie
        System.out.println("Mój string");

        //pojedynczy znak, np. 'a', '1' - w pojedynczym cudzysłowie
        System.out.println('a');

        //true/false
        System.out.println(true);

        //true/false
        System.out.println(false);




    }
}
